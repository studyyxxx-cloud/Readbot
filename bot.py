def сформировать_статистику(u):
    книги = u.get("книги", {})
    всего = len(книги)
    прочитала = len([b for b in книги.values() if b.get("статус") == "Прочитала"])
    читаю = len([b for b in книги.values() if b.get("статус") == "Читаю сейчас"])
    хочу = len([b for b in книги.values() if b.get("статус") == "Хочу прочитать"])
    пct = int(прочитала / всего * 100) if всего else 0
    бар = "🟩" * (пct // 10) + "⬜" * (10 - пct // 10)

    текст = f"📊 <b>Статистика {u.get('имя', '')}:</b>\n\n"
    текст += f"{бар} {пct}%\n\n"
    текст += f"✅ Прочитано: {прочитала}\n"
    текст += f"📖 Читаю сейчас: {читаю}\n"
    текст += f"📌 Хочу прочитать: {хочу}\n"
    текст += f"📚 Всего книг: {всего}\n\n"

    текст += "<b>По разделам:</b>\n"
    for раздел in u.get("разделы", []):
        иконка = ИКОНКИ_РАЗДЕЛОВ.get(раздел, "📚")
        кн = [b for b in книги.values() if b["раздел"] == раздел]
        пр = len([b for b in кн if b.get("статус") == "Прочитала"])
        текст += f"{иконка} {раздел}: {пр}/{len(кн)}\n"

    лог = u.get("лог", [])
    если_лог = len(лог)
    если_лог and (текст := текст + f"\n📅 Записей в дневнике: {если_лог}")
    return текст

def сформировать_достижения(u):
    все = {код: текст for код, текст, _ in ДОСТИЖЕНИЯ}
    полученные = u.get("achiev", [])
    текст = "🏆 <b>Достижения:</b>\n\n"
    if полученные:
        for код in полученные:
            текст += f"✅ {все.get(код, код)}\n"
    else:
        текст += "Пока нет достижений.\nЧитай книги и получай награды! 📚\n"
    текст += "\n<b>Ещё можно получить:</b>\n"
    for код, надпись, _ in ДОСТИЖЕНИЯ:
        if код not in полученные:
            текст += f"🔒 {надпись}\n"
    return текст

def сформировать_дневник(u):
    лог = u.get("лог", [])
    if not лог:
        return "📅 <b>Дневник чтения</b>\n\nПока нет записей.\nДобавь прогресс по книге!"
    текст = "📅 <b>Дневник чтения</b>\n\n"
    по_датам = {}
    for е in лог:
        д = е.get("дата", "")
        if д not in по_датам:
            по_датам[д] = []
        по_датам[д].append(е)
    for д in sorted(по_датам.keys(), reverse=True)[:7]:
        текст += f"📆 <b>{д}</b>\n"
        for е in по_датам[д]:
            bid = е.get("bid", "")
            название = u["книги"].get(bid, {}).get("название", bid.split("::")[-1])
            if е.get("страниц"):
                текст += f"  📄 {название}: {е['страниц']} стр.\n"
            elif е.get("минут"):
                текст += f"  🎧 {название}: {int(е['минут'])} мин.\n"
        текст += "\n"
    return текст

