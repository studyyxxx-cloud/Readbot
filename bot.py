# ИНДЕКСЫ КНИГ (вместо bid в callback_data)
# Telegram ограничивает callback_data — 64 байта.
# Кириллические bid весят до 135 байт — кнопки не работают.
# Передаём числовой индекс, по нему находим bid.
# ══════════════════════════════════════════
def страница_для_bid(u, bid, на_стр=7):
    """Вычисляет номер страницы раздела, на которой находится книга."""
    все_книги = list(u.get("книги", {}).items())
    раздел = u.get("книги", {}).get(bid, {}).get("раздел", "")
    книги_раздела = [b_id for b_id, b in все_книги if b["раздел"] == раздел]
    try:
        pos = книги_раздела.index(bid)
        return pos // на_стр
    except ValueError:
        return 0

def bid_по_индексу(u, idx):
    """Возвращает bid по числовому индексу в словаре книг."""
    try:
        return list(u.get("книги", {}).keys())[idx]
    except IndexError:
        return None

def индекс_по_bid(u, bid):
    """Возвращает числовой индекс bid в словаре книг."""
    keys = list(u.get("книги", {}).keys())
    try:
        return keys.index(bid)
    except ValueError:
        return None


