def сформировать_статистику(u):
    книги = u.get("книги", {})
    всего = len(книги)
    прочитала = len([b for b in книги.values() if b.get("статус") == "Прочитала"])
    читаю = len([b for b in книги.values() if b.get("статус") == "Читаю сейчас"])
    хочу = len([b for b in книги.values() if b.get("статус") == "Хочу прочитать"])
    процент = int(прочитала / всего * 100) if всего else 0
    бар = "🟩" * (процент // 10) + "⬜" * (10 - процент // 10)
    
    текст = f"📊 <b>Статистика</b>\n\n{бар} {процент}%\n\n✅ Прочитано: {прочитала}\n📖 Читаю: {читаю}\n📌 Хочу: {хочу}\n📚 Всего: {всего}"
    return текст

def сформировать_достижения(u):
    полученные = u.get("achiev", [])
    текст = "🏆 <b>Достижения</b>\n\n"
    if полученные:
        for код in полученные:
            текст += f"✅ {код}\n"
    else:
        текст += "Пока нет достижений. Читай книги! 📚"
    return текст

def сформировать_дневник(u):
    лог = u.get("лог", [])
    if not лог:
        return "📅 <b>Дневник</b>\n\nПока нет записей.\nДобавь прогресс по книге!"
    текст = "📅 <b>Дневник чтения</b>\n\n"
    for е in лог[-10:]:
        дата = е.get("дата", "")
        bid = е.get("bid", "")
        книга = u["книги"].get(bid, {})
        название = книга.get("название", bid.split("::")[-1])
        if е.get("страниц"):
            текст += f"📆 {дата}: 📄 {название} - {е['страниц']} стр.\n"
        elif е.get("минут"):
            текст += f"📆 {дата}: 🎧 {название} - {е['минут']} мин.\n"
    return текст

