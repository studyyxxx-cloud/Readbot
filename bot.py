def _check_streak(u, days):
    log = u.get("log", [])
    if not log:
        return False
    dates = sorted(set(e["date"] for e in log), reverse=True)
    if not dates:
        return False
    streak = 1
    for i in range(1, len(dates)):
        d1 = date.fromisoformat(dates[i-1])
        d2 = date.fromisoformat(dates[i])
        if (d1 - d2).days == 1:
            streak += 1
            if streak >= days:
                return True
        else:
            break
    return streak >= days

