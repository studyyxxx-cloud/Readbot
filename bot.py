def _check_streak(u, days):
    лог = u.get("лог", [])
    if not лог:
        return False
    dates = sorted(set(e["дата"] for e in лог if "дата" in e), reverse=True)
    if not dates:
        return False
    streak = 1
    for i in range(1, len(dates)):
        d1 = date.fromisoformat(dates[i-1])
        d2 = date.fromisoformat(dates[i])
        if (d1 - d2).days == 1:
            streak += 1
            if streak >= days:
                return True
        else:
            break
    return streak >= days

