состояния = {}

def уст_состояние(uid, состояние, данные=None):
    состояния[uid] = {"состояние": состояние, "данные": данные or {}}

def получить_состояние(uid):
    return состояния.get(uid, {})

def сбросить_состояние(uid):
    состояния.pop(uid, None)

def проверить_достижения(uid, u, chat_id):
    новые = []
    for код, текст, проверка in ДОСТИЖЕНИЯ:
        if код not in u["achiev"]:
            try:
                if проверка(u):
                    u["achiev"].append(код)
                    новые.append(текст)
            except Exception:
                pass
    if новые:
        сохранить(uid, u)
        отправить(chat_id, f"🏆 <b>Новое достижение!</b>\n\n" + "\n".join(новые))

