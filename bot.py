def отправить(chat_id, текст, кнопки=None):
    данные = {"chat_id": chat_id, "text": текст, "parse_mode": "HTML"}
    if кнопки:
        данные["reply_markup"] = json.dumps({"inline_keyboard": кнопки})
    try:
        requests.post(f"{API}/sendMessage", json=данные, timeout=10)
    except Exception as e:
        logging.error(f"Ошибка отправки: {e}")

def изменить(chat_id, msg_id, текст, кнопки=None):
    данные = {"chat_id": chat_id, "message_id": msg_id, "text": текст, "parse_mode": "HTML"}
    if кнопки:
        данные["reply_markup"] = json.dumps({"inline_keyboard": кнопки})
    try:
        requests.post(f"{API}/editMessageText", json=данные, timeout=10)
    except Exception as e:
        logging.error(f"Ошибка изменения: {e}")

