def показать_книгу(uid, chat_id, msg_id, u, bid, fn, sec_page=0):
    книга = u["книги"].get(bid)
    if not книга:
        return
    статус = книга.get("статус", "Хочу прочитать")
    форматы = книга.get("форматы", [])
    заметка = книга.get("заметка", "")
    иконка_ст = _иконка_статуса(статус)

    # Считаем прогресс
    лог_книги = [е for е in u.get("лог", []) if е["bid"] == bid]
    стр_прочит = sum(е.get("страниц", 0) for е in лог_книги)
    мин_прослуш = sum(е.get("минут", 0) for е in лог_книги)
    всего_стр = книга.get("страниц_всего", 0)
    всего_мин = книга.get("часов_всего", 0) * 60

    текст = f"{иконка_ст} <b>{книга['название']}</b>\n✍️ {книга.get('автор','')}\n"
    текст += f"📂 {книга['раздел']}\n"
    текст += f"Статус: {статус}\n"
    if форматы:
        текст += f"Формат: {', '.join(форматы)}\n"
    if стр_прочит > 0:
        прогресс = int(стр_прочит / всего_стр * 100) if всего_стр else 0
        бар = "🟩" * (прогресс // 10) + "⬜" * (10 - прогресс // 10)
        текст += f"\n{бар} {прогресс}%\nСтраниц: {стр_прочит}"
        if всего_стр:
            текст += f" из {всего_стр}"
    if мин_прослуш > 0:
        прогресс = int(мин_прослуш / всего_мин * 100) if всего_мин else 0
        бар = "🟩" * (прогресс // 10) + "⬜" * (10 - прогресс // 10)
        текст += f"\n{бар} {прогресс}%\nМинут: {int(мин_прослуш)}"
        if всего_мин:
            текст += f" из {int(всего_мин)}"
    if заметка:
        текст += f"\n💬 <i>{заметка}</i>"

    bidx = индекс_по_bid(u, bid)
    кнопки_статуса = []
    for i, ст in enumerate(СТАТУСЫ):
        отм = "✅ " if ст == статус else ""
        кнопки_статуса.append({"text": f"{отм}{ст}", "callback_data": f"st:{bidx}:{i}"})
    кнопки = [кнопки_статуса[:2], кнопки_статуса[2:],
        [{"text": "📖 Формат чтения", "callback_data": f"fmt:{bidx}"},
         {"text": "📊 Прогресс", "callback_data": f"prog:{bidx}"}],
        [{"text": "💬 Заметка", "callback_data": f"note:{bidx}"},
         {"text": "« Назад", "callback_data": f"sec:{РАЗДЕЛ_КОД.get(книга['раздел'], книга['раздел'][:10])}:{sec_page}"}],
    ]
    fn(chat_id, msg_id, текст, кнопки)

