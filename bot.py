def обработать_сообщение(msg):
    chat_id = msg["chat"]["id"]
    uid = msg["from"]["id"]
    текст = msg.get("text", "").strip()

    u = загрузить(uid)
    сост = получить_состояние(uid)

    # Первый вход — спрашиваем имя
    if not u.get("имя") and not сост:
        уст_состояние(uid, "ждём_имя")
        отправить(chat_id,
            "👋 <b>Привет, друг!</b>\n\n"
            "Я рад, что ты решил отслеживать своё чтение!\n\n"
            "Как тебя зовут? Напиши своё имя 😊"
        )
        return

    # Обработка состояний диалога
    if сост:
        обработать_состояние(uid, chat_id, текст, u, сост)
        return

    if текст == "/start":
        т, к = главное_меню(u)
        отправить(chat_id, т, к)
    elif текст == "/help":
        отправить(chat_id,
            "📚 <b>Команды бота:</b>\n\n"
            "/start — главное меню\n"
            "/progress — мой прогресс\n"
            "/stats — статистика\n"
            "/help — помощь\n\n"
            "Просто напиши название книги чтобы найти её!"
        )
    elif текст == "/progress" or текст == "/stats":
        отправить(chat_id, сформировать_статистику(u))
    else:
        # Поиск книги
        найдено = [(bid, b) for bid, b in u.get("книги", {}).items()
                   if текст.lower() in b["название"].lower() or текст.lower() in b.get("автор","").lower()]
        if найдено:
            все_ключи = list(u.get("книги", {}).keys())
            кнопки = [[{"text": f"{_иконка_статуса(b['статус'])} {b['название'][:40]}",
                        "callback_data": f"book:{все_ключи.index(bid)}"}] for bid, b in найдено[:6]]
            кнопки.append([{"text": "« Главное меню", "callback_data": "main"}])
            отправить(chat_id, f"🔍 Нашла {len(найдено)} книг:", кнопки)
        else:
            т, к = главное_меню(u)
            отправить(chat_id, т, к)

def обработать_состояние(uid, chat_id, текст, u, сост):
    шаг = сост["состояние"]
    данные = сост.get("данные", {})

    if шаг == "ждём_имя":
        u["имя"] = текст
        сохранить(uid, u)
        сбросить_состояние(uid)
        т, к = главное_меню(u)
        отправить(chat_id, f"✨ Отлично, <b>{текст}</b>! Добро пожаловать!\n\nВот твоя читалка:", к)
        # Отправляем меню отдельным сообщением чтобы кнопки отобразились
        отправить(chat_id, т, к)

    elif шаг == "ждём_название_книги":
        данные["название"] = текст
        уст_состояние(uid, "ждём_автора_книги", данные)
        отправить(chat_id, f"📝 Название: <b>{текст}</b>\n\nТеперь напиши автора (или напиши «-» если не знаешь):")

    elif шаг == "ждём_автора_книги":
        данные["автор"] = "" if текст == "-" else текст
        уст_состояние(uid, "ждём_раздел_книги", данные)
        разделы = u.get("разделы", [])
        кнопки = [[{"text": f"{ИКОНКИ_РАЗДЕЛОВ.get(р, '📚')} {р}", "callback_data": f"chsec:{si}"}]
                  for si, р in enumerate(разделы)]
        кнопки.append([{"text": "➕ Создать новый раздел", "callback_data": "newsec4book"}])
        отправить(chat_id, "В какой раздел добавить книгу?", кнопки)

    elif шаг == "ждём_новый_раздел":
        новый = текст.strip()
        if новый not in u["разделы"]:
            u["разделы"].append(новый)
        сохранить(uid, u)
        сбросить_состояние(uid)
        отправить(chat_id, f"✅ Раздел <b>{новый}</b> создан!", [[{"text": "« Главное меню", "callback_data": "main"}]])

    elif шаг == "ждём_раздел_для_новой_книги":
        раздел = текст
        данные["раздел"] = раздел
        _добавить_книгу(uid, chat_id, u, данные)

    elif шаг == "ждём_заметку":
        bid = данные["bid"]
        if bid in u["книги"]:
            u["книги"][bid]["заметка"] = текст
            сохранить(uid, u)
        сбросить_состояние(uid)
        отправить(chat_id, "💬 Заметка сохранена!", [[{"text": "« Главное меню", "callback_data": "main"}]])

    elif шаг == "ждём_страниц_всего":
        bid = данные["bid"]
        if текст.isdigit() and bid in u["книги"]:
            u["книги"][bid]["страниц_всего"] = int(текст)
            сохранить(uid, u)
            уст_состояние(uid, "ждём_прогресс_страниц", {"bid": bid})
            отправить(chat_id, f"Отлично! Сколько страниц прочитала <b>сегодня</b>?")
        else:
            отправить(chat_id, "Напиши число, например: 320")

    elif шаг == "ждём_часов_всего":
        bid = данные["bid"]
        try:
            часы = float(текст.replace(",", "."))
            u["книги"][bid]["часов_всего"] = часы
            сохранить(uid, u)
            уст_состояние(uid, "ждём_прогресс_часов", {"bid": bid})
            отправить(chat_id, f"Отлично! Сколько минут слушала <b>сегодня</b>?")
        except Exception:
            отправить(chat_id, "Напиши число часов, например: 4.5")

    elif шаг == "ждём_прогресс_страниц":
        bid = данные["bid"]
        if текст.isdigit():
            запись = {"дата": str(date.today()), "bid": bid,
                      "страниц": int(текст), "формат": "страницы"}
            u["лог"].append(запись)
            сохранить(uid, u)
            сбросить_состояние(uid)
            проверить_достижения(uid, u, chat_id)
            всего = u["книги"][bid].get("страниц_всего", 0)
            прочит = sum(е["страниц"] for е in u["лог"] if е["bid"] == bid and е.get("страниц"))
            прогресс = int(прочит / всего * 100) if всего else 0
            бар = "🟩" * (прогресс // 10) + "⬜" * (10 - прогресс // 10)
            отправить(chat_id,
                f"📄 Записано {текст} страниц!\n\n"
                f"{бар} {прогресс}%\n"
                f"Прочитано: {прочит} из {всего} стр.",
                [[{"text": "« Главное меню", "callback_data": "main"}]]
            )
        else:
            отправить(chat_id, "Напиши число страниц, например: 30")

    elif шаг == "ждём_прогресс_часов":
        bid = данные["bid"]
        try:
            минуты = float(текст.replace(",", "."))
            запись = {"дата": str(date.today()), "bid": bid,
                      "минут": минуты, "формат": "аудио"}
            u["лог"].append(запись)
            сохранить(uid, u)
            сбросить_состояние(uid)
            проверить_достижения(uid, u, chat_id)
            всего = u["книги"][bid].get("часов_всего", 0) * 60
            прослушано = sum(е["минут"] for е in u["лог"] if е["bid"] == bid and е.get("минут"))
            прогресс = int(прослушано / всего * 100) if всего else 0
            бар = "🟩" * (прогресс // 10) + "⬜" * (10 - прогресс // 10)
            отправить(chat_id,
                f"🎧 Записано {текст} минут!\n\n"
                f"{бар} {прогресс}%\n"
                f"Прослушано: {int(прослушано)} из {int(всего)} мин.",
                [[{"text": "« Главное меню", "callback_data": "main"}]]
            )
        except Exception:
            отправить(chat_id, "Напиши число минут, например: 45")

    elif шаг == "ждём_время_напоминания":
        try:
            части = текст.strip().split(":")
            час = int(части[0])
            минута = int(части[1]) if len(части) > 1 else 0
            if 0 <= час <= 23 and 0 <= минута <= 59:
                u["remind"] = f"{час:02d}:{минута:02d}"
                сохранить(uid, u)
                сбросить_состояние(uid)
                отправить(chat_id,
                    f"✅ Буду напоминать читать каждый день в <b>{час:02d}:{минута:02d}</b> 📚",
                    [[{"text": "« Главное меню", "callback_data": "main"}]]
                )
            else:
                отправить(chat_id, "Неверное время. Напиши в формате ЧЧ:ММ, например: 21:00")
        except Exception:
            отправить(chat_id, "Напиши время в формате ЧЧ:ММ, например: 21:00")

def _добавить_книгу(uid, chat_id, u, данные):
    раздел = данные.get("раздел", "Разное")
    название = данные.get("название", "")
    автор = данные.get("автор", "")
    bid = f"{раздел}::{название}"
    u["книги"][bid] = {
        "название": название, "автор": автор, "раздел": раздел,
        "статус": "Хочу прочитать", "форматы": [], "заметка": "",
        "страниц_всего": 0, "часов_всего": 0, "custom": True
    }
    if раздел not in u["разделы"]:
        u["разделы"].append(раздел)
    сохранить(uid, u)
    сбросить_состояние(uid)
    проверить_достижения(uid, u, chat_id)
    bidx = индекс_по_bid(u, bid)
    отправить(chat_id,
        f"✅ Книга добавлена!\n\n📖 <b>{название}</b>\n✍️ {автор}\n📂 {раздел}",
        [[{"text": "Открыть книгу", "callback_data": f"book:{bidx}"},
          {"text": "« Меню", "callback_data": "main"}]]
    )

