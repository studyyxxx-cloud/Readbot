def сформировать_статистику(u):
    книги = u.get("книги", {})
    всего = len(книги)
    прочитала = len([b for b in книги.values() if b.get("статус") == "Прочитала"])
    читаю = len([b for b in книги.values() if b.get("статус") == "Читаю сейчас"])
    текст = f"📊 Статистика\n\n✅ Прочитано: {прочитала}\n📖 Читаю: {читаю}\n📚 Всего: {всего}"
    return текст

def сформировать_достижения(u):
    полученные = u.get("achiev", [])
    текст = "🏆 Достижения:\n\n"
    for код in полученные:
        текст += f"✅ {код}\n"
    if not полученные:
        текст += "Пока нет достижений."
    return текст

def сформировать_дневник(u):
    лог = u.get("лог", [])
    if not лог:
        return "📅 Дневник пуст"
    текст = "📅 Дневник:\n\n"
    for е in лог[-10:]:
        текст += f"{е.get('дата')}: {е.get('страниц', е.get('минут', 0))}\n"
    return текст

