def обработать_кнопку(cq):
    chat_id = cq["message"]["chat"]["id"]
    msg_id = cq["message"]["message_id"]
    uid = cq["from"]["id"]
    д = cq["data"]
    ответить_кнопку(cq["id"])
    u = загрузить(uid)

    if д == "cancel_state":
        сбросить_состояние(uid)
        т, к = главное_меню(u)
        изменить(chat_id, msg_id, т, к)
        return

    if д == "main":
        сбросить_состояние(uid)
        т, к = главное_меню(u)
        изменить(chat_id, msg_id, т, к)
        return

    # ═══════════════════════════════════════
    # ОТОБРАЖЕНИЕ РАЗДЕЛА
    # ═══════════════════════════════════════
    if д.startswith("sec:"):
        код = д.split(":", 1)[1]
        
        страница = 0
        base_code = код
        if "p" in код:
            parts = код.split("p")
            if len(parts) == 2 and parts[1].isdigit():
                страница = int(parts[1])
                base_code = parts[0]
        
        раздел = КОД_РАЗДЕЛА.get(base_code, base_code)
        
        # Собираем книги раздела
        книги_раздела = []
        for bid, b in u.get("книги", {}).items():
            if b.get("раздел") == раздел:
                книги_раздела.append((bid, b))
        
        if not книги_раздела:
            иконка = ИКОНКИ_РАЗДЕЛОВ.get(раздел, "📚")
            отправить(chat_id, 
                f"{иконка} <b>{раздел}</b>\n\n📭 В этом разделе пока нет книг.",
                [[{"text": "« Назад", "callback_data": "main"}]]
            )
            return
        
        иконка = ИКОНКИ_РАЗДЕЛОВ.get(раздел, "📚")
        прочит = len([b for _, b in книги_раздела if b.get("статус") == "Прочитала"])
        
        НА_СТРАНИЦУ = 15
        всего = len(книги_раздела)
        начало = страница * НА_СТРАНИЦУ
        конец = min(начало + НА_СТРАНИЦУ, всего)
        стр_книги = книги_раздела[начало:конец]
        
        кнопки = []
        for bid, b in стр_книги:
            статус_книги = b.get("статус", "Хочу прочитать")
            if статус_книги == "Хочу прочитать":
                иконка_ст = "📌"
            elif статус_книги == "Читаю сейчас":
                иконка_ст = "📖"
            elif статус_книги == "Прочитала":
                иконка_ст = "✅"
            else:
                иконка_ст = "🚫"
            кнопки.append([{"text": f"{иконка_ст} {b['название'][:42]}", "callback_data": f"book:{bid}"}])
        
        # Пагинация
        навигация = []
        if страница > 0:
            навигация.append({"text": "◀️ Назад", "callback_data": f"sec:{base_code}p{страница-1}"})
        if конец < всего:
            навигация.append({"text": "Вперёд ▶️", "callback_data": f"sec:{base_code}p{страница+1}"})
        if навигация:
            кнопки.append(навигация)
        
        кнопки.append([{"text": "« Главное меню", "callback_data": "main"}])
        
        текст_раздела = f"{иконка} <b>{раздел}</b>\n✅ Прочитано: {прочит} из {всего}"
        if всего > НА_СТРАНИЦУ:
            текст_раздела += f"\n📄 Страница {страница+1} из {((всего-1)//НА_СТРАНИЦУ)+1}"
        
        # Отправляем НОВОЕ сообщение
        отправить(chat_id, текст_раздела, кнопки)
        return

    # ═══════════════════════════════════════
    # ОТОБРАЖЕНИЕ КНИГИ
    # ═══════════════════════════════════════
    if д.startswith("book:"):
        bid = д.split(":", 1)[1]
        if bid not in u.get("книги", {}):
            отправить(chat_id, "❌ Книга не найдена", [[{"text": "Меню", "callback_data": "main"}]])
            return
        показать_книгу(uid, chat_id, None, u, bid, отправить)
        return

    # ═══════════════════════════════════════
    # ИЗМЕНЕНИЕ СТАТУСА
    # ═══════════════════════════════════════
    if д.startswith("st:"):
        parts = д.split(":", 2)
        bid = parts[1]
        новый_статус = СТАТУСЫ[int(parts[2])]
        if bid in u["книги"]:
            u["книги"][bid]["статус"] = новый_статус
            сохранить(uid, u)
            проверить_достижения(uid, u, chat_id)
        показать_книгу(uid, chat_id, None, u, bid, отправить)
        return

    # ═══════════════════════════════════════
    # ФОРМАТЫ
    # ═══════════════════════════════════════
    if д.startswith("fmt:"):
        bid = д.split(":", 1)[1]
        книга = u["книги"].get(bid)
        if книга:
            кнопки = []
            for fi, ф in enumerate(ФОРМАТЫ):
                отмечен = "✅ " if ф in книга.get("форматы", []) else ""
                кнопки.append([{"text": f"{отмечен}{ф}", "callback_data": f"tf:{bid}:{fi}"}])
            кнопки.append([{"text": "« Назад", "callback_data": f"book:{bid}"}])
            отправить(chat_id, f"📖 <b>Форматы</b>\n{книга['название']}", кнопки)
        return

    if д.startswith("tf:"):
        parts = д.split(":", 2)
        bid = parts[1]
        формат = ФОРМАТЫ[int(parts[2])]
        if bid in u["книги"]:
            форматы = u["книги"][bid].get("форматы", [])
            if формат in форматы:
                форматы.remove(формат)
            else:
                форматы.append(формат)
            u["книги"][bid]["форматы"] = форматы
            сохранить(uid, u)
        показать_книгу(uid, chat_id, None, u, bid, отправить)
        return

    # ═══════════════════════════════════════
    # ЗАМЕТКА
    # ═══════════════════════════════════════
    if д.startswith("note:"):
        bid = д.split(":", 1)[1]
        уст_состояние(uid, "ждём_заметку", {"bid": bid})
        отправить(chat_id, "💬 Напиши заметку:", [[{"text": "❌ Отмена", "callback_data": "cancel_state"}]])
        return

    # ═══════════════════════════════════════
    # УПРАВЛЕНИЕ
    # ═══════════════════════════════════════
    if д == "bkmenu":
        отправить(chat_id, "📚 Управление книгами:", [
            [{"text": "➕ Добавить книгу", "callback_data": "addbook"}],
            [{"text": "« Назад", "callback_data": "main"}]
        ])
        return

    if д == "addbook":
        уст_состояние(uid, "ждём_название_книги")
        отправить(chat_id, "📝 Напиши название книги:", [[{"text": "❌ Отмена", "callback_data": "cancel_state"}]])
        return

    if д == "secmenu":
        отправить(chat_id, "📂 Управление разделами:", [
            [{"text": "➕ Добавить раздел", "callback_data": "addsec"}],
            [{"text": "« Назад", "callback_data": "main"}]
        ])
        return

    if д == "addsec":
        уст_состояние(uid, "ждём_новый_раздел")
        отправить(chat_id, "📂 Напиши название нового раздела:", [[{"text": "❌ Отмена", "callback_data": "cancel_state"}]])
        return

    if д.startswith("chsec:"):
        idx = int(д.split(":", 1)[1])
        раздел = u.get("разделы", [])[idx]
        сост = получить_состояние(uid)
        данные = сост.get("данные", {})
        данные["раздел"] = раздел
        _добавить_книгу(uid, chat_id, u, данные)
        return

    if д == "newsec4book":
        сост = получить_состояние(uid)
        данные = сост.get("данные", {})
        уст_состояние(uid, "ждём_раздел_для_новой_книги", данные)
        отправить(chat_id, "📂 Напиши название нового раздела:", [[{"text": "❌ Отмена", "callback_data": "cancel_state"}]])
        return

    # ═══════════════════════════════════════
    # СТАТИСТИКА, ДОСТИЖЕНИЯ, ДНЕВНИК
    # ═══════════════════════════════════════
    if д == "stats":
        отправить(chat_id, сформировать_статистику(u), [[{"text": "« Назад", "callback_data": "main"}]])
        return

    if д == "achiev":
        отправить(chat_id, сформировать_достижения(u), [[{"text": "« Назад", "callback_data": "main"}]])
        return

    if д == "diary":
        отправить(chat_id, сформировать_дневник(u), [[{"text": "« Назад", "callback_data": "main"}]])
        return

    if д == "remind":
        отправить(chat_id, "⏰ Напоминания пока не настроены.\nДобавлю позже!", [[{"text": "« Назад", "callback_data": "main"}]])
        return

    # Если ничего не подошло — показываем меню
    т, к = главное_меню(u)
    отправить(chat_id, т, к)

def показать_книгу(uid, chat_id, msg_id, u, bid, fn):
    книга = u["книги"].get(bid)
    if not книга:
        fn(chat_id, "❌ Книга не найдена", [[{"text": "Меню", "callback_data": "main"}]])
        return
    
    статус = книга.get("статус", "Хочу прочитать")
    if статус == "Хочу прочитать":
        иконка_ст = "📌"
    elif статус == "Читаю сейчас":
        иконка_ст = "📖"
    elif статус == "Прочитала":
        иконка_ст = "✅"
    else:
        иконка_ст = "🚫"
    
    форматы = книга.get("форматы", [])
    текст = f"{иконка_ст} <b>{книга['название']}</b>\n✍️ {книга.get('автор', '')}\n📂 {книга['раздел']}\n📌 Статус: {статус}\n"
    if форматы:
        текст += f"📖 Формат: {', '.join(форматы)}\n"
    if книга.get("заметка"):
        текст += f"\n💬 {книга['заметка']}"
    
    кнопки_статуса = []
    for i, ст in enumerate(СТАТУСЫ):
        отм = "✅ " if ст == статус else ""
        кнопки_статуса.append({"text": f"{отм}{ст}", "callback_data": f"st:{bid}:{i}"})
    
    код_раздела = РАЗДЕЛ_КОД.get(книга['раздел'], книга['раздел'][:10])
    кнопки = [
        кнопки_статуса[:2],
        кнопки_статуса[2:],
        [{"text": "📖 Формат чтения", "callback_data": f"fmt:{bid}"},
         {"text": "💬 Заметка", "callback_data": f"note:{bid}"}],
        [{"text": "« Назад", "callback_data": f"sec:{код_раздела}"}],
    ]
    
    if msg_id is None:
        отправить(chat_id, текст, кнопки)
    else:
        изменить(chat_id, msg_id, текст, кнопки)

