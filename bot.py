def проверить_достижения(uid, u, chat_id):
    новые = []
    for код, текст, проверка in ДОСТИЖЕНИЯ:
        if код not in u["achiev"]:
            try:
                if проверка(u):
                    u["achiev"].append(код)
                    новые.append(текст)
            except Exception:
                pass
    if новые:
        сохранить(uid, u)
        отправить(chat_id, f"🏆 <b>Новое достижение!</b>\n\n" + "\n".join(новые))

