def проверить_напоминания():
    сейчас = datetime.now()
    сегодня = str(сейчас.date())
    текущее_время = f"{сейчас.hour:02d}:{сейчас.minute:02d}"
    for файл in os.listdir(DATA_DIR):
        if not файл.endswith(".json"):
            continue
        try:
            uid = int(файл.replace(".json", ""))
            u = загрузить(uid)
            напом = u.get("remind")
            if not напом or напом != текущее_время:
                continue
            # Проверяем: не отправляли ли уже сегодня в это время
            последнее = u.get("remind_sent", "")
            ключ_дня = f"{сегодня}:{текущее_время}"
            if последнее == ключ_дня:
                continue  # уже отправляли сегодня
            # Запоминаем в JSON — выживет после перезапуска
            u["remind_sent"] = ключ_дня
            сохранить(uid, u)
            имя = u.get("имя", "")
            читаю = [b for b in u.get("книги", {}).values() if b.get("статус") == "Читаю сейчас"]
            текст = f"📚 <b>Время читать, {имя}!</b>\n\n"
            if читаю:
                текст += "Ты сейчас читаешь:\n"
                for б in читаю[:3]:
                    текст += f"• {б['название']}\n"
            отправить(uid, текст)
        except Exception as e:
            logging.error(f"Ошибка напоминания: {e}")

    # Итоги недели — воскресенье в 20:00
    if сейчас.weekday() == 6 and сейчас.hour == 20 and сейчас.minute == 0:
        week_key = f"{сейчас.year}:{сейчас.isocalendar()[1]}"
        for файл in os.listdir(DATA_DIR):
            if not файл.endswith(".json"):
                continue
            try:
                uid = int(файл.replace(".json", ""))
                ключ = f"{uid}:{week_key}"
                if ключ in последние_итоги:
                    continue
                последние_итоги[ключ] = True
                u = загрузить(uid)
                лог = u.get("лог", [])
                неделя_назад = str(date.fromisocalendar(сейчас.year, сейчас.isocalendar()[1], 1))
                записи_недели = [е for е in лог if е.get("дата", "") >= неделя_назад]
                if записи_недели:
                    стр = sum(е.get("страниц", 0) for е in записи_недели)
                    мин = sum(е.get("минут", 0) for е in записи_недели)
                    дней = len(set(е["дата"] for е in записи_недели))
                    текст = f"📊 <b>Итоги недели, {u.get('имя', '')}!</b>\n\n"
                    текст += f"📅 Читала {дней} дней из 7\n"
                    if стр:
                        текст += f"📄 Прочитано страниц: {стр}\n"
                    if мин:
                        текст += f"🎧 Прослушано минут: {int(мин)}\n"
                    текст += "\nОтличная неделя! Продолжай! 🔥"
                    отправить(uid, текст)
            except Exception as e:
                logging.error(f"Ошибка итогов недели: {e}")

